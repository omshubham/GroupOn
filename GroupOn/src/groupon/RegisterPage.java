    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        // TODO add your handling code here:
        String fullname=reg_fullname.getText();
        String username=reg_username.getText();
        String reg_no=reg_regno.getText();
        String date_of_birth=reg_dob.getText();
        String country=reg_country.getSelectedItem().toString();
        String college=reg_college.getText();
        Long contact=Long.parseLong(reg_contactno.getText());
        String password=reg_pwd.getText();
        conn=MysqlConnect.ConnectDB();
        try {
            pst=conn.prepareStatement("insert into login_info(username,password,full_name,registration_no,dob,country,college,contact_no) values(?,?,?,?,?,?,?,?)");
            pst.setString(1,username);
            pst.setString(2,password);
            pst.setString(3,fullname);
            pst.setString(4,reg_no);
            pst.setString(5,date_of_birth);
            pst.setString(6,country);
            pst.setString(7,college);
            pst.setString(8,contact.toString());
            
            
            int i=pst.executeUpdate();
            if(i>0) {
                JOptionPane.showMessageDialog(null, "you are successfully registered");
                JOptionPane.showMessageDialog(null, "you can now Login");
        reg_fullname.setText(null);
        reg_username.setText(null);
        reg_regno.setText(null);
        reg_dob.setText(null);
        reg_country.setSelectedIndex(0);
        reg_college.setText(null);
        reg_contactno.setText(null);
        reg_pwd.setText(null);
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_registerActionPerformed

